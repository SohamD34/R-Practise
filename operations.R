# Vector arithmetic operations

v <- c( 2,5.5,6)
t <- c(8, 3, 4)
print(v+t)  # add
print(t-v)  # subtract
print(v*t)  # multiply
print(t/v)  # division - complete float ans
print(t%%v) # remainder
print(t%/%v) # int division
print(v^t)  # v raised to power t



# Relational operations (comparison)

print(v>t)    # [1] FALSE  TRUE  TRUE
print(v<=t)   # [1]  TRUE FALSE FALSE
print(v==t)   # [1] FALSE FALSE FALSE
print(v!=t)   # [1] TRUE TRUE TRUE


